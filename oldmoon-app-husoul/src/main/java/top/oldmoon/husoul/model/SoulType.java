package top.oldmoon.husoul.model;

public class SoulType {
    private String id;
    private String type_name;
    private String type_no;
    private String sort;
    private String soul_num;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType_name() {
        return type_name;
    }

    public void setType_name(String type_name) {
        this.type_name = type_name;
    }

    public String getType_no() {
        return type_no;
    }

    public void setType_no(String type_no) {
        this.type_no = type_no;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getSoul_num() {
        return soul_num;
    }

    public void setSoul_num(String soul_num) {
        this.soul_num = soul_num;
    }
}
